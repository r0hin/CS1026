grade = input("Enter your letter grade: ")

if grade == "A":
  print("Excellent, you got an A!")
elif grade == "B":
  print("Good, you got a B!")
elif grade == "C":
  print("Not bad. You got a C.")
elif grade == "D":
  print("You passed. You've got a D.")
elif grade == "F":
  print("You failed. You've got an F.")
else:
  print("Invalid grade.")